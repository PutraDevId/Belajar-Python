# operasi logika atau boolean

'''
1. not (adalah mengubah nilai menjadi berlawanan atau membalikan nilai, contohnya jika nilai true kita kasih not maka akan flase begitu sebaliknya)
2. or  (akan menghasilkan nilai false jika kedua buah nilai false)
3. and (akan true jika kedua nya true)
4. xor ^ (sama seperti or tapi jika keduanya bernilai true maka akan false dan jika keduanya false  maka akan false)

'''

# contoh
a = True
b = False

c = a or b  # akan true
print(c)

d = a ^ b
print(d)
